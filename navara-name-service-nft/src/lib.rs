#[derive(BorshSerialize, BorshStorageKey)]
enum StorageKey {
    NonFungibleToken,
    Metadata,
    TokenMetadata,
    Enumeration,
    Approval,
}

#[ext_contract(ext_name_resolver)]
pub trait NameResolver {
    fn owner_changed(
        &mut self,
        new_owner: AccountId
    );
}

#[near_bindgen]
impl Contract {
    /// Initializes the contract owned by `owner_id` with
    /// default metadata (for example purposes only).
    #[init]
    pub fn new_default_meta(owner_id: AccountId) -> Self {
        Self::new(
            owner_id,
            NFTContractMetadata {
                spec: NFT_METADATA_SPEC.to_string(),
                name: "Example NEAR non-fungible token".to_string(),
                symbol: "EXAMPLE".to_string(),
                icon: Some(DATA_IMAGE_SVG_NEAR_ICON.to_string()),
                base_uri: None,
                reference: None,
                reference_hash: None,
            },
        )
    }

    #[init]
    pub fn new(owner_id: AccountId, metadata: NFTContractMetadata) -> Self {
        require!(!env::state_exists(), "Already initialized");
        metadata.assert_valid();
        Self {
            tokens: NonFungibleToken::new(
                StorageKey::NonFungibleToken,
                owner_id,
                Some(StorageKey::TokenMetadata),
                Some(StorageKey::Enumeration),
                Some(StorageKey::Approval),
            ),
            metadata: LazyOption::new(StorageKey::Metadata, Some(&metadata)),
        }
    }

    /// Mint a new token with ID=`token_id` belonging to `token_owner_id`.
    ///
    /// Since this example implements metadata, it also requires per-token metadata to be provided
    /// in this call. `self.tokens.mint` will also require it to be Some, since
    /// `StorageKey::TokenMetadata` was provided at initialization.
    ///
    /// `self.tokens.mint` will enforce `predecessor_account_id` to equal the `owner_id` given in
    /// initialization call to `new`.
    #[payable]
    pub fn nft_mint(
        &mut self,
        token_id: TokenId,
        token_owner_id: AccountId,
    ) -> Token {
        assert_eq!(env::predecessor_account_id(), self.tokens.owner_id, "Unauthorized");

        let token_metadata = TokenMetadata {
            title: Some(format!("{}.nns", token_id)),
            description: Some("Global name service powered by NEAR protocol".to_string()),
            media: Some(DATA_IMAGE_SVG_NEAR_ICON.to_string()),
            media_hash: None,
            copies: None,
            issued_at: None,
            expires_at: None,
            starts_at: None,
            updated_at: None,
            extra: None,
            reference: None,
            reference_hash: None
        };
        self.tokens.internal_mint(token_id.to_owned(), token_owner_id, Some(token_metadata))
    }

    #[payable]
    pub fn setup(&mut self, token_id: TokenId) {
        let deposited = env::attached_deposit();
        let resolver_account_id = format!("{}.{}", token_id, env::current_account_id()).parse().unwrap();
        let account_id = env::predecessor_account_id();

        let resolver_arg = ResolverArgs {
            owner_id: account_id
        };
        
        let required_balance = self.get_min_attached_balance(&resolver_arg);

        Promise::new(resolver_account_id)
            .create_account()
            .transfer(required_balance)
            .deploy_contract(RESOLVER_WASM_CODE.to_vec())
            .function_call("new".to_owned(), serde_json::to_vec(&resolver_arg).unwrap(), 0, GAS);
    }

    fn get_min_attached_balance(&self, args: &ResolverArgs) -> u128 {
        ((RESOLVER_WASM_CODE.len() + EXTRA_BYTES + args.try_to_vec().unwrap().len() * 2) as Balance
            * STORAGE_PRICE_PER_BYTE + 5)
            .into()
    }

    pub fn nft_owner_changed(&self, resolver: AccountId, new_owner: AccountId) -> Promise {
        ext_name_resolver::ext(resolver)
            .with_static_gas(env::prepaid_gas())
            .owner_changed(new_owner)
    }
}

